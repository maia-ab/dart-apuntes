main() {
  int a = 10 + 5;
  a = 20 - 10;
  a = 10 * 2;

  double b = 10 / 2; // Division.
  b = 10.0 % 3; // Modulo (resto de la division).
  b = -b; // -expr Es usado para cambir el valor de la expresion.

  int c = 10 ~/ 3; // ~/ Division entera.

  int d = 1;

  d++; // Suma 1.
  d--; // Resta 1.

  d += 2; //  Suma un valor x al valor actual de la variable.
  d -= 2; //  Resta un valor x al valor actual de la variable.
}
