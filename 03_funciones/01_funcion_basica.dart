main() {
  // Todo dentro de las llaves es el cuerpo de la funcion.
  var mensaje = saludar(); // Invoco la funcion.

  print(mensaje);
}

String saludar() {
  // Declaro la funcion.
// Si no retorna nada se declara como void.
// Cuando la funcion es void el valor de retorno sera null.

  return "Hola Fernando";
}
